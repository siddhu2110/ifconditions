class Percentage_Standard{
  static int a=100;
  static char b='A';
   public static void main(String[]args){
      if(a>=85 && a<=100 && b == b){
          System.out.println("Very High Standard");
        }
      else if(a>=70 && a<=84 && b == b){
          System.out.println("High Standard");

        }
      else if (a>=50 && a<=69 && b == b){
        System.out.println("standard");
        }

   }
  }